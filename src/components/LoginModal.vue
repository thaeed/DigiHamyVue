<template>
  <div class="row justify-content-center">
    <div class="wrapper fadeInDown">
      <div id="formContent">
        <!-- Tabs Titles -->

        <!-- Icon -->
        <h5>[درخواست کد ورود]</h5>
        <br />
        <!-- Login Form -->
        <form>
          <div class="container">
            <div class="row align-items-center">
              <div class="container">
                <div class="col">
                  <div
                    v-if="showSuccess"
                    class="alert alert-success"
                    role="alert"
                  >
                    کد ورود برای شما ارسال شد
                  </div>
                </div>
              </div>
            </div>
            <div class="row align-items-center">
              <div class="col"></div>
              <div class="col-12">
                <div class="input-group mb-3">
                  <input
                    dir="ltr"
                    type="text"
                    @keypress="isNumber($event)"
                    v-model="txtPhone"
                    maxlength="9"
                    class="form-control shadow-none"
                    style="
                      padding: 0px;
                      margin: 0px;
                      text-align: left;
                      letter-spacing: 0.7em;
                      font-size: large;
                    "
                    placeholder="شماره خود را وارد کنید"
                  />
                  <div class="input-group-append">
                    <span class="input-group-text">09</span>
                  </div>
                </div>
              </div>
              <div class="col"></div>
            </div>
            <div class="row">
              <div class="container">
                <div class="col">
                  <div v-if="showError" class="alert alert-danger" role="alert">
                    شماره وارد شده نامعتبر است.
                  </div>
                </div>
              </div>
            </div>
            <div class="row">
              <div class="col-8">
                <button
                  name="btnRequestCode"
                  type="button"
                  class="btn --digiGreen btn-block"
                  @click="requestCode"
                >
                  {{ btnMessage }}
                </button>
              </div>

              <div class="col-4">
                <button type="button" class="btn --digiOrange" @click="close">
                  انصراف
                </button>
              </div>
            </div>
          </div>
        </form>
      </div>
    </div>
  </div>
</template>

<script>
import axios from "axios";
export default {
  name: "Modal",
  data() {
    return {
      showError: false,
      showSuccess: false,
      txtPhone: "",
      btnMessage: "تایید شماره",
      timerCount: 10,
      timerEnabled: false,
      apiResponse: null,
    };
  },
  watch: {
    timerEnabled(value) {
      if (value) {
        setTimeout(() => {
          this.timerCount--;
        }, 1000);
      }
    },

    timerCount: {
      handler(value) {
        if (value > 0 && this.timerEnabled) {
          setTimeout(() => {
            this.timerCount--;
            this.btnMessage = "(ارسال مجدد کد.. " + this.timerCount + ")";
          }, 1000);
        } else {
          this.timerCount = 10;
          this.timerEnabled = false;
          this.btnMessage = "تایید شماره";
        }
      },
      immediate: true, // This ensures the watcher is triggered upon creation
    },
  },
  methods: {
    close() {
      this.$emit("close");
    },

    isNumber: function (evt) {
      evt = evt ? evt : window.event;
      var charCode = evt.which ? evt.which : evt.keyCode;
      if (
        charCode > 31 &&
        (charCode < 48 || (charCode > 57 && charCode != 127)) &&
        charCode !== 46
      ) {
        evt.preventDefault();
      } else {
        return true;
      }
    },
    requestCode() {
      if (this.timerEnabled == false) {
        this.showError = false;
        this.showSuccess = false;
        const regex = /09(1[0-9]|3[1-9]|2[1-9])-?[0-9]{3}-?[0-9]{4}/;
        const matches = regex.test("09" + this.txtPhone);
        console.log(matches);
        if (matches === false) {
          this.showError = true;
          return;
        }

        this.timerEnabled = true;

        axios
          .get(
            "https://api.digi-hamy.ir/user/requestcode/" + "09" + this.txtPhone,
            {
              headers: {
                ApiKey: "ouur12%^074$!^0QWYE#!WU78rt12bU!gg&",
              },
            }
          )
          .then((res) => {
            this.apiResponse = res.data;
          });

        console.log(this.apiResponse);
        if(this.apiResponse['status'] == true)
        this.showSuccess = true;
      }
    },
  },
};
</script>

